// Helper function to find country by phone code
export const findCountryByCode = (phoneCode: string): string | null => {
  for (const [countryName, info] of Object.entries(COUNTRY_CODES)) {
    if (info.code === phoneCode) {
      return countryName;
    }
  }
  return null;
};

// Helper function to validate phone code
export const isValidPhoneCode = (phoneCode: string): boolean => {
  return Object.values(COUNTRY_CODES).some(info => info.code === phoneCode);
};
